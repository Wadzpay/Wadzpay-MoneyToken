    parent_id             smallint,
    sorting               numeric(3, 2),
    created_updated_at    timestamp NULL,
    created_updated_by    bigint,
    status                boolean,
    constraint modules_transaction_pkey primary key (module_transaction_id),
    CONSTRAINT fk_modules_transaction_created_updated_by FOREIGN KEY (created_updated_by) REFERENCES user_account (id)

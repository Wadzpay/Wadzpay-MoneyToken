create table payment_poc_logs (
    id bigint generated by default as identity not null,
    user_account_id bigint not null,
    user_email varchar(100),
    transaction_type varchar(100),
    curr_code varchar(100),
    amount numeric(40,20),
    description varchar(255),
    narration varchar(244),
    merchant_order_token varchar(100),
    ord_title varchar(100),
    success_url  varchar,
    fail_url varchar,
    pg_cancel_url varchar,
    queue_if_low_balance varchar,
    status varchar,
    response_data varchar,
    transaction_id varchar,
    pay_id varchar,
    reference_id varchar,
    message varchar,
    status_code varchar,
    ref_link varchar,
    order_id varchar,
    payment_type varchar,
    upi varchar,
    account_number varchar,
    ifsc_code varchar,
    beneficiary_name varchar,
    payment_mode varchar,
    created_at timestamp,
    updated_at timestamp,
    wadzpay_trx_id VARCHAR,
    primary key (id),
    CONSTRAINT fk_user_id FOREIGN KEY (user_account_id) REFERENCES user_account (id)
);
